// Função para remover acentos, substituir hífens por espaço, colapsar espaços e padronizar para minúsculas
function normalizarTexto(texto) {
    return texto
        .normalize("NFD")                      // Decompor acentos
        .replace(/[\u0300-\u036f]/g, "")         // Remover acentos
        .replace(/-/g, " ")                      // Substituir hífen por espaço
        .replace(/\s+/g, " ")                    // Colapsar múltiplos espaços em um só
        .trim()                                  // Remover espaços extras das pontas
        .toLowerCase();                          // Converter para minúsculas
}

// Criar um novo objeto com chaves normalizadas
const chasNormalizado = {};
for (const chave in chas) {
    chasNormalizado[normalizarTexto(chave)] = chas[chave];
}

function buscarCha() {
    const inputElement = document.getElementById("chaInput");
    const inputNormalizado = normalizarTexto(inputElement.value);
    const resultadoDiv = document.getElementById("resultado");

    if (chasNormalizado[inputNormalizado]) {
        const cha = chasNormalizado[inputNormalizado];
        resultadoDiv.innerHTML = `
            <h2>${inputElement.value.charAt(0).toUpperCase() + inputElement.value.slice(1)}</h2>
            <p><strong>Benefícios:</strong> ${cha.beneficios}</p>
            <p><strong>Malefícios:</strong> ${cha.maleficios}</p>
            <img src="${cha.imagem}" alt="Imagem do chá ${inputElement.value}">
        `;
    } else {
        resultadoDiv.innerHTML = "<p>Chá não encontrado. Tente outro nome.</p>";
    }

    inputElement.value = "";
}

// Adiciona os event listeners para acionar a busca ao pressionar Enter e ao clicar no botão
document.addEventListener("DOMContentLoaded", function() {
    const inputElement = document.getElementById("chaInput");
    const searchButton = document.querySelector("button"); // Se preferir, adicione um id ao botão e use getElementById

    // Dispara a busca ao pressionar Enter no input
    inputElement.addEventListener("keydown", function(event) {
        if (event.key === "Enter") {
            buscarCha();
        }
    });

    // Dispara a busca ao clicar no botão
    searchButton.addEventListener("click", buscarCha);
});
